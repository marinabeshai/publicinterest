class Congress:
   # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------    
    def __init__(self, number,  senate_party, senate_members, house_party, house_members):
        self.number = number 
        self.senate_party = senate_party
        self.senate_members = senate_members
        self.house_party = house_party
        self.house_members = house_members
    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
  
    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    def debug(self):
        return(
            "self.number :{}\n num_of_senators: {}\n senate_party: {}\nsenate_members: {}\nnum_of_representatives: {}\nhouse_party: {}\nhouse_members: {}".format(self.number, self.get_count_senate(), self.senate_party, self.senate_members, self.get_count_house(), self.house_party, self.house_members)
        )
   # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------

   # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    def get_count_total(self):    
        return len(self.house_members) + len(self.senate_members)
    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    def get_count_senate(self): 
        return len(self.senate_members)
    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    def get_count_house(self):        
        return len(self.house_members) 
    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------

    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------
    def get_everyone(self):
        return self.senate_members + self.house_members
    # -----------------------------------------------------------------------------------------------------------------------------------------------------------------------------

